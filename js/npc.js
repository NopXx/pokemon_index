if (style_image != null) {
  console.log(style_image)
  if (style_image === '1') {
      document.getElementById('image-default').checked = true;
  } else {
      document.getElementById('image-pix').checked = true;
  }
} else {
  localStorage.setItem('style-image', 1)
  document.getElementById('image-default').checked = true;
}

